// only number concate
var eggPrice = 50;
var orangePrice = 50;
console.log(eggPrice + orangePrice);

// number + string
var eggPrice = 50;
var orangePrice = "50";
console.log(orangePrice + eggPrice);

// parseInt
var eggPrice = 50;
var orangePrice = parseInt("50");
console.log(orangePrice + eggPrice);

// parseFLoat
var eggPrice = 50;
var orangePrice = parseFloat("50.2");
console.log(orangePrice + eggPrice);

var eggPrice = 0.1;
var orangePrice = 0.2;
var total = eggPrice + orangePrice;
console.log(total.toFixed(2));
console.log(total.toFixed(5));


